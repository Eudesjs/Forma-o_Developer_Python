'''OPERADORES MATEMATICOS
Os operadores aritmeticos em python permitem realizar operaçoes matematicas
basicas, como adição, subtração, multiplicação, divisão e exponenciação são
ferramentas essenciais para qualquer programador python, desde iniciantes 
ate experientes.
'''
# SOMA ( + )
soma = 5 + 2
print(soma)

# SUBTRAÇÃO ( - )
menos = 5 - 2
print(menos)

# MULTIPLICAÇÃO ( * )
vezes = 5 * 2
print(vezes) 

# DIVISÃO ( / )
dividir = 5 / 2
print(dividir)

# PARTE INTEIRA DA DIVISÃO ( // )
inteiro = 5 // 2
print(inteiro)

# RESTO DA DIVISÃO ( % )
resto = 5 % 2
print(resto)

# EXPONENCIAÇÃO ( ** )
elevado = 5 ** 2
print(elevado)

